                                <p>
                                    The temple is a shining example of
                                    <strong class="architecture-style">Kalinga architecture</strong>, a style developed in ancient Odisha. Constructed during the 12th century by
                                    <strong class="king-name">King Anantavarman Chodaganga Deva</strong> of the
                                    <strong class="dynasty">Eastern Ganga dynasty</strong>
