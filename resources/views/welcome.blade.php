                                </p>
                                <h3 class="place-title">üåä 4. Chilika Lake ‚Äì Asia's Largest Brackish Water Lagoon</h3>
                                <p class="place-description">
                                    Just a short drive from Puri (about 37 km),
                                    <strong class="place-name">Chilika Lake</strong> is Asia's largest coastal lagoon and a paradise for
                                    <span class="interest">nature lovers</span> and
                                    <span class="interest">bird watchers</span>. This vast water body stretches across the Khurda, Ganjam, and Puri districts and is home to over 160 species of
                                    <strong class="wildlife-highlight">migratory birds</strong>
                                    during winter.
                                </p>
                                <p class="place-description">
                                    The lake's highlight is
                                    <strong class="highlight-text">Satapada</strong>, where you can take boat rides to watch the endangered
                                    <strong class="wildlife-highlight">Irrawaddy dolphins</strong> in their natural habitat. The combination of pristine waters, peaceful islands, and rich biodiversity makes it a must-visit for
                                    <span class="interest">eco-tourism lovers</span>
                                    .
                                </p>
                                <p class="place-description">
                                    Don't forget to explore
                                    <strong class="temple-name">Kalijai Temple</strong> located on an island within the lake, which is dedicated to
                                    <strong class="deity-name">Goddess Kalijai</strong>
                                    ‚Äì the presiding deity of the fishermen.
                                </p>
                                <h3 class="place-title">‚òÄÔ∏è 5. Konark Sun Temple ‚Äì The UNESCO World Heritage Wonder</h3>
                                <p class="place-description">
                                    Located about 35 km from Puri, the
                                    <strong class="temple-name">Konark Sun Temple</strong> is one of the most iconic monuments in India and a
                                    <strong class="highlight-text">UNESCO World Heritage Site</strong>. Built in the 13th century by
                                    <strong class="historical-figure">King Narasimhadeva I</strong> of the Eastern Ganga dynasty, this temple is dedicated to the
                                    <strong class="deity-name">Sun God</strong> and is an architectural marvel of the
                                    <strong class="architecture">Kalinga style</strong>
                                    .
                                </p>
                                <p class="place-description">
                                    The temple is designed in the form of a colossal
                                    <strong class="symbolic-element">chariot</strong>
                                    with twelve intricately carved wheels and pulled by seven horses, symbolizing the sun's journey across the sky. Though much of the original structure is now in ruins, the surviving artwork and sculpture are breathtakingly beautiful.
                                </p>
                                <p class="place-description">
                                    It's also the venue for the annual
                                    <strong class="festival-name">Konark Dance Festival</strong>
                                    , which brings together classical dancers from across the country. If you're visiting Puri, a day trip to Konark is an absolute must.
